#List acts like a container which stores values in the same order they are entered
#Creating a list using [] method
a = [1,2,3,44,2]

#Print list using print() function
print(a)
#Access elements using index
print(a[1])
print(a[3])

#Change the list value using
a[4]=100 
print(a)

#We can create a list with items of different types
b = ["I", "Love", "Python", 1, 10.3, True]
print(b)
b[2] = "Cricket"
print(b)

