#Dictonary is a collection of key-value pairs

myDict = {
    "Name" : "Sharanbasappa",
    "LastName" : "Simikore", 
    "Ages" : [21, 23, 22], #List
    "anotherDict" : {"Dance", "Harry"},
    "Dico" : {"AnnaBond" : "Nanu"}
}

print(myDict["Name"])
print(myDict["LastName"])
print(myDict["Ages"]) #Integers in Dictonary
print(myDict["anotherDict"])
print(myDict["Dico"]["AnnaBond"]) #Nested Dictonary